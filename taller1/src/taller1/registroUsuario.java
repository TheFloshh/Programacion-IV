    private void btnValidarActionPerformed() {
        String idValide, nameValide, lasNameValide, ageValide;        

        idValide = this.txtId.getText();
        nameValide = this.txtName.getText();
        lasNameValide = this.txtLastName.getText();
        ageValide = this.txtCorreo.getText();
        String button = this.btnValidar.getText();

        if(idValide.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Por favor ingrese identificador");
        }else if(nameValide.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Por favor ingrese nombre");
        }else if(lasNameValide.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Por favor ingrese apellido");
        }else if(ageValide.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Por favor ingrese edad");
        }else{
            
            if(button.equalsIgnoreCase("Validar")){
                JOptionPane.showMessageDialog(rootPane, "Campos validados correctamente");
                validar = true;
            }
            
        }
    }

