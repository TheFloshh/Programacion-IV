    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed

        btnValidarActionPerformed();       
        String button = this.btnValidar.getText();
        
        if (validar && button.equalsIgnoreCase("Validar")){
            btnValidar.setText("Back");
            txtId.enable(false);
            txtName.enable(false);
            txtLastName.enable(false);
            txtCorreo.enable(false);
        }else{
            btnValidar.setText("Validar");
            txtId.enable(true);
            txtName.enable(true);
            txtLastName.enable(true);
            txtCorreo.enable(true);
            validar = false;
        }
        

    }//GEN-LAST:event_btnValidarActionPerformed

